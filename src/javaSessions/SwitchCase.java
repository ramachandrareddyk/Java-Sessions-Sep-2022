package javaSessions;

public class SwitchCase {

	public static void main(String[] args) {
		String browser="chrome";
		
		switch (browser) {
		case "chrome":{
			System.out.println("Launch chrome browser");
		}case "firefox":{
			System.out.println("Launch chrome browser");
		}case "Ie":{
			System.out.println("Launch chrome browser");
		}case "safari":{
			System.out.println("Launch chrome browser");
		}
		default:
		System.out.println("Please pass the correct browser");
		}

	}

}
